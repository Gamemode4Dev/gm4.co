      <section id="server">
        <h2>Server Rules</h2>
        <p>
          While on the public Gamemode 4 server or any official GM4 server, we have a few other rules that must be followed in addition to the ones above! These are in place to allow everyone to enjoy the server, ensure a fair experience, and help keep things organised. When playing with others, remember everyone has their own playstyles and actions, and that PVP is not allowed without their explicit consent first.
        </p>

        <h3>No Cheating, Hacking, or use of Disallowed Modifications</h3>
        <p>To keep the server fair for everyone, you may not grant yourself an unfair advantage or put others at a disadvantage. Any form of client or client modification that provides gameplay-altering advantage or significant non-vanilla features is not allowed, as is the use of:</p>
        <ul>
          <li>Bugs or Exploits</li>
          <li>Macros or hotkeys</li>
          <li>X-Ray texturepacks, mods, or vision</li>
          <li>
            In-Game Duplication Exploits*<br>
            <i>* Duplication may only be allowed if it's carpets specifically for map art!</i>
          </li>
        </ul>
        <h3>Claims and Ownership</h3>
        <p>
          Our server does not use claim or ownership plugins; instead, you may claim any location of reasonable size and place your belongings anywhere, as long as you do not break any other rules.<br />
          Mark your claim and belongings so they stand out as player-made
          Claims must be smaller in high-traffic areas
          We reserve the right to revoke your ownership of a claim if you are no longer playing regularly. For high-traffic areas, playing regularly refers to you having been engaged with the server within the last 30 days; for all other areas, 90 days.
          If you notice a vacant build, you may contact the owner or staff, who may be able to revoke previous ownership if you have a good reason to take over the claim or items.
        </p>
        <h3>Respect Others' Builds</h3>
        <p>
          Build a reasonable distance away from others' builds and respect the terrain, surroundings, and appearance of nearby builds.<br />
          Do not crowd, cover, block, or deface others' builds.
        </p>
        <h3>No Stealing</h3>
        <p>
          Stealing in any shape or form is not allowed. This means you cannot take anything that is not yours, regardless of whether you know who owns it. This includes taking items from an unlabeled chest or containers you might find around.
        </p>
        <h3>No Griefing</h3>
        <p>
          Griefing, i.e. modifying another player's build without permission, is not allowed on the server. Griefing includes but is not limited to:
        <ul>
          <li>Placing or removing blocks</li>
          <li>Activating or modifying redstone</li>
          <li>Moving or killing entities</li>
        </ul>
        <h3>Excessive Lag</h3>
        <p>
          Do not cause lag. Builds that cause lag will be warnable, and we reserve the right to kick you and remove builds in the case of excessive lag. This means you should take actions to keep entity counts low and ensure farms are well-regulated. Complex redstone contraptions should also be kept away, and chunk loaders are not allowed.
        </p>
        <p>
          Ender Pearl stasis chambers are allowed, as long as they do not cause excessive lag.
        </p>
