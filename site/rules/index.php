        <h2>Staff</h2>
        <p>
          Staff members can be recognised by their Discord role and in-game prefix. Only staff are allowed to enforce these rules. If you see others breaking them, please get in touch with staff via Discord immediately and let them handle it.<br />
          Do not ask for staff; only active community members selected by the current staff team receive staff. There is no way to apply for staff directly.
        </p>
